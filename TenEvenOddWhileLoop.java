package homework2;

public class TenEvenOddWhileLoop {
    public static void main(String[] args) {
        int a = 2;
        while(a<=20){
            System.out.println("Even Number = " +a);
            a++;
            a++;
        }
        int b = 1;
        while (b<=20){
            System.out.println("Odd Number = " +b);
            b++;
            b++;
        }
    }
}
