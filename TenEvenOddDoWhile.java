package homework2;

public class TenEvenOddDoWhile {
    public static void main(String[] args) {
        int a=2;
        do{
            System.out.println("Even Number = "+a);
            a++;
            a++;
        }while (a<=20);
        int b=1;
        do{
            System.out.println("Odd Number = "+b);
            b++;
            b++;
        }while (b<=20);
    }
}
